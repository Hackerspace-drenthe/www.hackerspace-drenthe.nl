		<p>
			Welkom bij de website van de hackerspace Drenthe. <br>
			Onze hackerspace is op dit moment (juli 2020) in oprichting. <br>
		</p>
		
		<h2>Waarom een hackerspace in Drenthe?</h2>
		<p>
			Als creatieve techneuten hebben wij de behoefte om elkaar regelmatig te zien, idee&euml;n uit te wisselen en van elkaar te leren.<br>
			Vanuit Emmen zijn de dichtsbijzijnde hackerspaces in Enschede, Zwolle, Groningen en Lingen (D).<br> 
			Dat is voor de Drentse techno-hobbyist net iets te ver rijden om regelmatig langs te gaan.<br>
			Daarom zijn we nu bezig om een hackerspace in de regio van Emmen te starten van/voor/door Drentse techneuten. 
		</p>

		<h2>Huidige stand van zaken (1 juli 2020)</h2>
		<p>
			Op 1 juni 2020 zijn we voor het eerst bij elkaar geweest om te bepalen hoe we gaan starten. Daaruit zijn een paar aktiepunten gekomen.
			<ul>
				<li>mensen betrekken bij ons plan (prio 1)</li>
				<li>kernteam regelen van 5-7 personen (prio 2)</li>
				<li>website / wiki maken</li>
				<li>mailinglijst maken</li>
				<li>irc kanaal maken  [DONE]-> #hackerspace-drenthe (<a href="https://webchat.oftc.net/">irc.oftc.net</a>)</li>
				<li>BBQ organiseren</li>
				<li>Locatie zoeken</li>
			</ul>
		</p>

		<h2>Hulp gevraagd</h2>
		<p>
			We zijn nog maar net begonnen en er is nog veel te doen. Daarom zijn we op zoek naar mensen die ons hierbij willen helpen.<br>
			Lijkt het je leuk om mee te bouwen en deel te nemen aan onze hackerspace neem dan contact op.<br>
			Iedereen is welkom.
		</p>
	
